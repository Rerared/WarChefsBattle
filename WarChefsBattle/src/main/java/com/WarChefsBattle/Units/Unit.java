package com.WarChefsBattle.Units;

public class Unit {
	private int attack = 0;
	private int hp = 0;
	private String typeAttack = "";
	private String typeDefence = "";
	public int getAttack() {
		return attack;
	}
	public void setAttack(int attack) {
		this.attack = attack;
	}
	public int getHp() {
		return hp;
	}
	public void setHp(int hp) {
		this.hp = hp;
	}
	public String getTypeAttack() {
		return typeAttack;
	}
	public void setTypeAttack(String typeAttack) {
		this.typeAttack = typeAttack;
	}
	public String getTypeDefence() {
		return typeDefence;
	}
	public void setTypeDefence(String typeDefence) {
		this.typeDefence = typeDefence;
	}
	
	
}
